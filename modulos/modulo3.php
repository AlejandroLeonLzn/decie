                <h1 class="display-4">NECESIDAD DEL MERCADO</h1>
              <p>
              Ya tienes una idea muy clara del giro de tu negocio, cuándo iniciarás y lo que ofrecerás al consumidor, pero ¿te has puesto a pensar realmente cuáles son las necesidades que tiene la población de la zona en la que te establecerás?
              <br>
              <br><br>La competencia: hay que tener muy claro que nuestro producto o servicio no será el único en el mercado. Date una vuelta por la zona e investiga si lo que ofreces a tu consumidor tiene competencia.
              <br><br>Producto nuevo: si estás pensando ofrecer un nuevo producto en el mercado o bien abrir un negocio, revisa qué tan necesario es lo que vas a ofrecerles a los consumidores de la zona, pues tal vez tu idea es muy buena pero no está dentro de las posibilidades o prioridades de las personas. La innovación es importante, claro, pero hay otros factores que deberás tomar en cuenta.
              <br><br>Necesidades del consumidor: las necesidades de consumo, son muy diversas y específicas, para conocer cuáles son, puedes hacer uso de técnicas de investigación como aplicar encuestas, entrevistas o “focus group” por mencionar algunas; éstas te servirán para saber si tu producto satisface al consumidor o bien, si es necesario que modifiques algo en lo que ofreces.
              <br><br>La publicidad: ésta es una buena aliada para todo negocio, pues será a partir de ésta que darás a conocer el producto, servicio o bien ofertados. La publicidad te funcionaría también para que los consumidores tengan presente tu negocio. Puedes acercarte a un especialista para que realice la publicidad y promoción adecuada y que además se adapte a presupuesto.
              <br><br>Escucha a tus clientes: la mejor publicidad es aquella que se realiza de boca en boca, es importante que en todo momento escuches a tu consumidor, pues ellos pueden decirte de forma directa qué es lo que necesitan. Recuerda que estás ofreciendo algo a las personas que te generará un beneficio.
              </p>
