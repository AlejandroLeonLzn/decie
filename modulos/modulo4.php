              <h1 class="display-4">CANALES DE DISTRIBUCIÓN</h1>
              <br>
              <p>
              Un canal de distribución consiste en el conjunto de personas y empresas comprendidas en la transferencia de derechos de un producto al paso de éste del productor al consumidor o usuario de negocios final
<br>

<h2>DISEÑO DE DISTRIBUCIÓN </h2>
<br><br>
<h3>1.	ESPECIFIQUE LA FUNCIÓN DE LA DISTRIBUCIÓN. 
</h3>
<br><br>
Se debe diseñar una estrategia de canal dentro del contexto de la mezcla de marketing entera. Primero se revisan los objetivos de marketing de la compañía. Luego se especifican las funciones asignadas al producto, el precio y la promoción.
<br><br>
<h3>2.	SELECCIONE EL TIPO DE CANAL. 
</h3>
<br><br>
Una vez que se ha acordado la función de la distribución en el programa total de marketing, hay que determinar el tipo más conveniente de canal para el producto de la compañía
<br><br>
<h3>3.	DETERMINE LA INTENSIDAD DE LA DISTRIBUCIÓN.
</h3>
<br><br>
La siguiente decisión se relaciona con la intensidad de la distribución, o sea, el número de intermediarios que se emplearán en los niveles de ventas al mayoreo y al detalle en un territorio particular.
<br><br>
<h3>4.	ELIJA MIEMBROS ESPECÍFICOS DEL CANAL
</h3>
<br><br>
La última decisión concierne a la selección de compañías específicas que distribuyan el producto<br>
Es el circuito mediante el cual un producto llega al consumidor final. Existen dos tipos de canales de distribución:
<br>
1.	DIRECTOS<br>
Es aquel en donde el producto o fabricante vende directo al cliente<br><br>
2.	INDIRECTOS<br>
Es cuando se requiere de algún intermediario. Si la demanda es muy elevada es necesario apoyarse de un tercero.<br><br>

</p>
