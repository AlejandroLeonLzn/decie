                <h1 class="display-4">COSTOS</h1>
                
                <p>
                Los costos se definen como los gastos económicos que representa la fabricación de un producto o prestación de un servicio. Al determinar el costo de producción, se puede establecer el precio de venta al público. Los costos se clasifican en: 
                  <br><br>
                A. Costos de Fabricación: Estos costos de se forman a través de Costos Fijos + Costos Variables <br>
                B. Costos Variables: Son aquellos que dependen del volumen de producción<br>
                C. Costos de producción: Son aquellos costos aplicables a empresas de transformación y que están integrados por los tres elementos del costo: Costo de materiales, sueldos y salarios directos y gastos indirectos de producción.<br>
                D. Costos de operación: Son aquellos que tienen por objeto determinar lo que cuesta: administrar, vender y financiar un producto o un servicio; esto a su vez pueden ser: Costo de Administración, de Distribución<br>
                E. Costos extraordinarios: Son aquellos costos no ordinarios.<br>
                </p>
                
