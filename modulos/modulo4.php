              <h1 class="display-4">NEGOCIACIÓN</h1>
              
              <p>
              Es el proceso donde las partes interesadas resuelven conflictos, buscan obtener ventajas (individuales o colectivas) o procuran obtener resultados que sirvan a sus intereses.
              <br>
              Una buena negociación representa un ingreso a la empresa, por lo cual es importante tener empatía con el prospecto y lograr el cierre de venta.
              <h2>
              TIPOS DE NEGOCIACIÓN</h2><br>
              <table border="1">
              <tr><td><h4>NEGOCIACIÓN ACERTADA</h4></td>
              <td>Se produce cuando las negociaciones obtienen todos o la mayoría de resultados que desean sin llegar a romper relaciones</td></tr>
              <tr><td><h4>NEGOCIACIÓN TRADICIONAL</h4></td>
              <td>Se denomina Ganar-Perder, debido al estilo de dureza del negociador que busca imponerse al otro.</td></tr>
              <tr><td><h4>NEGOCIACIÓN GANAR-GANAR</h4></td>
              <td>Las 2 Partes quedan satisfechas con los beneficios obtenidos, evita conflictos futuros (beneficios mutuos)</td></tr>
              <tr><td><h4>NEGOCIACIÓN INMEDIATA</h4></td>
              <td>Busca llegar con rapidez a un acuerdo sin preocuparse en tratar de establecer una relación personal con la otra parte.</td></tr>
              <tr><td><h4>NEGOCIACIÓN PROGRESIVA</h4></td>
              <td>Busca un aprovechamiento gradual y es muy importante la relación personal con el interlocutor </td></tr>
              <tr><td><h4>NEGOCIACIÓN ENFOCADA A RESULTADOS</h4></td>
              <td>Lo único que realmente le importa es alcanzar su objetivo a toda costa.</td></tr>
              <tr><td><h4>ESTRATEGIAS DE LA NEGOCIACIÓN</h4></td>
              <td>Es la manera en que cada en que da parte trata de conducirse con el fin de alcanzar sus objetivos </td></tr>
              <tr><td rowspan="2"><h4>ESTRATEGIAS TÍPICAS</h4></td>
              <td>1.	Ganar-Ganar Es la que busca que ambas partes ganen, compartiendo el beneficio.</td></tr><tr>
              <td>2.	Ganar-Perder Cada parte trata de alcanzar el máximo beneficio a costa del oponente, crea un ambiente de confrontación.
              </td></tr>
