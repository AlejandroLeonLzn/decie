                <h1 class="display-4">COMPETENCIA</h1>

              <p>
              La competencia es la empresa o conjunto de empresas que producen o venden productos similares o sustitutos a los que una empresa produce o vende en el mismo mercado.
              <br><br>
              La competencia puede ser directa cuando se trata de empresas que producen o venden productos similares a los de uno, o indirecta cuando se trata de empresas que producen o venden productos sustitutos a los de uno.
<br><br>
•	Competencia basada en precios Reducción de costos para atraer consumidores, se traduce en reducción de precios.
<br>
•	Competencia basada en servicios (Innovaciones, mejoras en la entrega del bien o servicio, ofertas de mayor calidad, etc.)
