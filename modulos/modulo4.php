                <h1 class="display-4">CLIENTES
</h1>
               <p>
               Cliente es la persona, empresa u organización que adquiere o compra de forma voluntaria productos o servicios que necesita o desea para sí mismo, para otra persona o para una empresa u organización
<br>
<h2>TIPOS DE CLIENTES
</h2><br>
<br>
<h3>CLIENTES ACTUALES</h3><br>
Son aquellas personas, empresas u organizaciones que le hacen compras a la empresa de forma periódica o que lo hicieron en una fecha reciente. 
<br>
De acuerdo a una clasificación más específica los clientes actuales se dividen en:
<br>

<h3>CLIENTES ACTIVOS E INACTIVOS</h3><br>
Los clientes activos son aquellos que en la actualidad están realizando compras o que lo hicieron dentro de un periodo corto de tiempo. 
<br>
Los clientes inactivos son aquellos que realizaron su última compra hace bastante tiempo atrás<br>
<h3>CLIENTES DE COMPRA FRECUENTE, PROMEDIO Y OCASIONAL
</h3><br>
Una vez que se han identificado a los clientes activos, se los puede clasificar según su frecuencia de compra, en:
<br>
CLIENTES DE COMPRA FRECUENTE<br>
Son aquellos que realizan compras repetidas a menudo o cuyo intervalo de tiempo entre una compra y otra es más corta que el realizado por el grueso de clientes.
<br>
CLIENTES DE COMPRA HABITUAL<br>
Son aquellos que realizan compras con cierta regularidad porque están satisfechos con la empresa, el producto y el servicio.
<br>
CLIENTES DE COMPRA OCASIONAL<br>
Son aquellos que realizan compras de vez en cuando o por única<br>

<h3>CLIENTE DE ALTO PROMEDIO Y BAJO VOLUMEN DE COMPRAS</h3><br>
Luego de identificar a los clientes activos y su frecuencia de compra, se puede realizar la siguiente clasificación (según el volumen de compras)
<br>
CLIENTES CON ALTO VOLUMEN DE COMPRAS<br>
Son aquellos que realizan compras en mayor cantidad que el grueso de clientes, a tal punto, que su participación en las ventas totales puede alcanzar entre 50 y 80%. 
<br>
<br>
CLIENTES CON PROMEDIO VOLUMEN DE COMPRAS<br>
Son aquellos que realizan compras en un volumen que está dentro del promedio general. 
<br>
<br>

CLIENTES CON BAJO VOLUMEN DE COMPRAS<br>
Son aquellos cuyo volumen de compras está por debajo del promedio, por lo general, a este tipo de clientes pertenecen los de compra ocasional.
<br>
<br>
<h3>CLIENTES COMPLACIDOS, SATISFECHOS E INSATISFECHO</h3><br>
Después de identificar a los clientes activos e inactivos, y de realizar una investigación de mercado que haya permitido determinar sus niveles de satisfacción, se los puede clasificar en:
<br>
Clientes Complacidos: Son aquellos que percibieron que el desempeño de la empresa, el producto y el servicio han excedido sus expectativas
<br>
Clientes satisfechos: Son aquellos que percibieron el desempeño de la empresa, el producto y dispuesto a cambiar de marca, pero puede hacerlo si encuentra otro proveedor ofrezca que el servicio como coincidente con sus expectativas.
<br>
Clientes Insatisfechos: Son aquellos que percibieron el desempeño de la empresa, el producto y/o el servicio por debajo de sus expectativas
<br>
Clientes Influyentes: Es su grado de influencia en la sociedad o en su entorno social. Este tipo de clientes se dividen en:       
<br>
Clientes Altamente influyentes: Este tipo de clientes se caracteriza por producir una percepción positiva o negativa en un grupo grande de personas hacia un producto o servicio. 
<br>
Clientes de Regular Influencia: Son aquellos que ejercen una determinada influencia en grupos mas reducidos
<br>
Clientes de Influencia a Nivel Familiar: Son aquellos que tienen un grado de influencia en su entorno de familiares y amigos
<br>

<h3>CLIENTES POTENCIALES</h3><br>
Son aquellos (personas, empresas u organizaciones) que no le realizan compras a la empresa en la actualidad pero que son visualizados como posibles clientes en el futuro porque tienen la disposición necesaria, el poder de compra y la autoridad para comprar. 

