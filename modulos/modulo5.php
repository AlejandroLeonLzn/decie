                <p>
                Es un conjunto de operaciones que son necesarias para llevar a cabo la transformación y elaboración de un producto o el diseño de un servicio.
               <br>
               <h2>TIPOS DE PROCESOS PRODUCTIVOS</h2><br>
               Es un conjunto de operaciones que son necesarias para llevar a cabo la transformación y elaboración de un producto o el diseño de un servicio.
               <br><br>
               A.	Producción bajo pedido: en esta modalidad productiva solamente se fabrica un producto a la vez y cada uno es diferente, no hay dos iguales, por lo que se considera un proceso de mano de obra intensiva
               <br>B.	Producción por lotes: con la frecuencia que sea necesario se produce una pequeña cantidad de productos idénticos. 
               <br>C.	Producción en masa: es como se denomina a la manufactura de cientos de productos idénticos, por lo general en una línea de fabricación. 
               <br>D.	Producción continua: permite fabricar muchos miles de productos idénticos y, a diferencia de la producción en masa, en este caso la línea de producción se mantiene en funcionamiento 24 horas al día, siete días a la semana
               <br><br>
               El proceso de producción consta de tres elementos:<br><br>
              1.	Insumos: material inicial que se incorpora al proceso para su transformación.<br>
              2.	Producto: resultado final de un sistema de producción.<br>
              3.	Operaciones: etapas del proceso de transformación necesarias para convertir insumos en productos terminados.
              <br><br>
              Existen dos sistemas de producción primordialmente:<br><br>

              1.	PRODUCCIÓN CONTINÚA<br>
              Las materias primas se reciben de los proveedores, luego se almacenan y pasan al proceso productivo, que sigue un itinerario prefijado. <br>
              La empresa puede dedicarse a producir sin descanso. Se requiere mano de obra especializada y no especializada.
              <br><br>
              2.	DE PRODUCCIÓN INTERMITENTE<br>
              Se produce o elabora un lote de productos que tiene un nivel predeterminado de producción, a continuación, se produce o elabora utilizando total o parcialmente las instalaciones y con el mismo personal, otro lote de otro producto, que tiene otro nivel de producción. <br>
              La mano de obra es más especializada y su costo es mayor.<br>
