<h2>PUNTO DE EQUILIBRIO</h2>
<br><br>
En términos de contabilidad de costos, es aquel punto de actividad (volumen de ventas) donde los ingresos totales son iguales a los costos totales, es decir, el punto de actividad donde no existe utilidad ni pérdida
<br><br>
<h3>PASOS PARA HALLAR EL PUNTO DE EQUILIBRIO</h3>
<br><br>
1. Definir costos: Es considerar como costos a todos los desembolsos, incluyendo los gastos de administración y de ventas, pero sin incluir los gastos financieros ni a los impuestos.
<br>
2. Clasificar los costos en Costos Variables (CV) y en Costos Fijos (CF)
<br><br>
a. Costos Variables: son los costos que varían de acuerdo con los cambios en los niveles de actividad, están relacionados con el número de unidades vendidas
<br>
b. Costos Fijos: son costos que no están afectados por las variaciones en los niveles de actividad
<br><br>
3. Hallar el costo variable unitario: En tercer lugar determinamos el Costo Variable Unitario (Cvu), el cual se obtiene al dividir los Costos Variables totales entre el número de unidades a producir (Q).
<br>
4.	Aplicar la fórmula del punto de equilibrio
<br><br>
La fórmula para hallar el punto de equilibrio es:
<br><br>
(P x U) – (Cvu x U) – CF = 0
<br><br>
Donde: 
<br><br>
P: precio de venta unitario. 
<br>
U: unidades del punto de equilibrio, es decir, unidades a vender de modo que los ingresos sean iguales a los costos. 
<br>
Cvu: costo variable unitario. 
<br>
CF: costos fijos. 
<br><br>
5. Comprobar resultados: Una vez hallado el punto de equilibrio, pasamos a comprobar el resultado a través del uso del Estado de Resultados. 
<br>
6. Analizar el punto de equilibrio: Y, por último, una vez hallado el punto de equilibrio y comprobado a través del Estado de Resultados, pasamos a analizarlo
