                  <h2>Definición de pasivos</h2>
                  <br><br>
                  Consiste en las deudas que la empresa posee, comprende las obligaciones actuales de la compañía que tienen origen en transacciones financieras pasadas
                  <br><br>
                  • Pasivo Circulante: Grupo formado por las deudas que tiene que pagar la empresa a corto plazo<br>
                  • Pasivo Fijo: Lo integran deudas que deben cubrirse a largo plazo <br>
                  • Pasivo Diferido: Bajo este rubro se presentan los ingresos que recibe la empresa anticipadamente a la prestación de sus servicios 
                  <br><br>
                  <h2>Definición de Capital</h2>
                  <br><br>
